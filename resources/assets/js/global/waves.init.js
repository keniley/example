function wavesInit() 
{
    Waves.attach('.waves-button-light',['waves-float','waves-light']);
    Waves.attach('.waves-button-dark',['waves-float']);
    Waves.attach('.waves-circle-light',['waves-circle','waves-float','waves-light']);
    Waves.attach('.waves-circle-dark',['waves-circle','waves-float']);
    Waves.init();
}

wavesInit();